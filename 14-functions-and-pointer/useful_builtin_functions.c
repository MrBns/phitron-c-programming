#include <stdio.h>
#include <string.h>
#include <stdlib.h>
#include <math.h>

int main()
{

    int num = 3.3;

    // make float to upper int;
    printf("%0.lf\n", ceil(num));

    // make float to down int;
    printf("%lf\n", floor(num));

    // make float to nearest int;
    printf("%lf", round(num));

    // power two number;
    

    // make a squreroot;
    printf("%lf", sqrt(num));



    return 0;
}